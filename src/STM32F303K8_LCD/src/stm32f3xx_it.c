extern uint8_t led;
extern TIM_HandleTypeDef htim3;
extern uint8_t pixy;
extern uint8_t pixx;
extern uint8_t scl;
extern uint8_t sda;
extern uint8_t csx;
extern uint8_t pix_idx;
extern uint16_t pix_buffer;
extern uint16_t pix_buffer_idx;

static const PixInfo_t* pixInfo = NULL;
static LineInfo_t* lineInfo = NULL;

extern PixInfo_t pixInfos0[];
extern PixInfo_t pixInfos1[];
extern PixInfo_t pixInfos2[];
extern PixInfo_t pixInfos3[];
extern PixInfo_t pixInfos4[];

extern LineInfo_t lineInfos[];

/**
 * @brief This function handles TIM3 global interrupt.
 */
void TIM3_IRQHandler(void)
{
    /* USER CODE BEGIN TIM3_IRQn 0 */
    if(scl == 1 && pixInfo != NULL && lineInfo != NULL) {
        sda = (pix_buffer >> 15) & 1;
        pix_buffer >>= 1;
        pix_buffer_idx++;
        pix_buffer_idx %= 16;
    }
    else if(scl == 0){
        if(!pix_buffer_idx) {
            if(pixInfo == NULL || pixInfo->length <= pixx) {
                if(lineInfo == NULL || lineInfo->num_pixInfo <= pix_idx) {
                    pix_idx = 0;
                    lineInfo = &lineInfos[pixy++];
                    pixy %= 5;
                }
                pixInfo = &(lineInfo->pixinfos[pix_idx++]);
            }
            pix_buffer = pixInfo->pixinfo;
            pixx++;
            csx = 0;
        }
    }
    if(lineInfo != NULL && pixInfo != NULL) {
        HAL_GPIO_WritePin(LCD_SDA_GPIO_Port, LCD_SDA_Pin, sda);
        HAL_GPIO_WritePin(LCD_SCK_GPIO_Port, LCD_SCK_Pin, scl);
    }
    


    /* HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, led); */
    led = 1 - led;

    /* USER CODE END TIM3_IRQn 0 */
    HAL_TIM_IRQHandler(&htim3);
    /* USER CODE BEGIN TIM3_IRQn 1 */

    /* USER CODE END TIM3_IRQn 1 */
}

